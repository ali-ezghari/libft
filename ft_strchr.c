#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0' && s[i] != (unsigned char)c)
	{
		i++;
	}
	if ((unsigned char)c == s[i])
		return ((char *)s + i);
	return (NULL);
}
